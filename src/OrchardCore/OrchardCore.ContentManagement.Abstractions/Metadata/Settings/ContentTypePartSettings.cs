namespace OrchardCore.ContentManagement.Metadata.Settings
{
    public class ContentTypePartSettings
    {
        /// <summary>
        /// Gets or sets the displayed name of the type part.
        /// </summary>
        public string DisplayName { get; set; }

        /// <summary>
        /// Gets or sets the description name of the type part.
        /// </summary>
        public string Description { get; set; }

        /// <summary>
        /// Gets or sets the position of the type part in the editor.
        /// </summary>
        public string Position { get; set; }

        /// <summary>
        /// Gets or sets the display mode of the type part.
        /// </summary>
        public string DisplayMode { get; set; }

        /// <summary>
        /// Gets or sets the editor of the type part.
        /// </summary>
        public string Editor { get; set; }
    }
}
